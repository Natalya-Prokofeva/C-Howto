using System;

class Program
{
    static void Main()
    {
        // Начальные условия
        int[,] numbers = new int[,] {
            {1, 2, 3},
            {1, 1, 0},
            {7, 8, 2},
            {9, 10, 11}
        };

        // Ищем строку с наименьшей суммой элементов
        int минСумма = int.MaxValue; // Инициализируем минимальную сумму максимальным значением
        int строкаСМинСуммой = -1; // Переменная для хранения индекса строки с минимальной суммой

        for (int i = 0; i < numbers.GetLength(0); i++) // Проходим по каждой строке
        {
            int сумма = 0; // Переменная для хранения суммы элементов текущей строки

            for (int j = 0; j < numbers.GetLength(1); j++) // Проходим по каждому элементу в строке
            {
                сумма += numbers[i, j]; // Добавляем значение элемента к сумме строки
            }

            if (сумма < минСумма) // Если текущая сумма меньше минимальной
            {
                минСумма = сумма; // Обновляем минимальную сумму
                строкаСМинСуммой = i; // Обновляем индекс строки с минимальной суммой
            }
        }

        // Выводим результат
        Console.WriteLine($"Строка с наименьшей суммой элементов: {строкаСМинСуммой}");
    }
}
